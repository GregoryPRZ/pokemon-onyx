def translate_pokemon_names_in_file(file_path):
    """
    Traduit tous les noms de Pok√©mon dans un fichier.
    """
    print(f"Traitement du fichier: {file_path}")
    
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
    except UnicodeDecodeError:
        with open(file_path, 'r', encoding='latin-1') as f:
            content = f.read()
    
    original_content = content
    changes_made = 0
    
    # Pattern pour trouver les .speciesName = _("nom")
    pattern = r'\.speciesName = _\("([^"]+)"\)'
    
    def replace_name(match):
        nonlocal changes_made
        original_name = match.group(1)
        
        if original_name in POKEMON_TRANSLATIONS:
            french_name = POKEMON_TRANSLATIONS[original_name]
            if french_name != original_name:  # Seulement compter si c'est vraiment un changement
                changes_made += 1
                print(f"  {original_name} ‚Üí {french_name}")
            return f'.speciesName = _("{french_name}")'
        else:
            print(f"  ‚ö†Ô∏è  Nom non trouv√© dans la liste: {original_name}")
            return match.group(0)  # Garder l'original
    
    # Remplacer tous les noms
    content = re.sub(pattern, replace_name, content)
    
    # Sauvegarder seulement si des changements ont √©t√© faits
    if content != original_content:
        try:
            with open(file_path, 'w', encoding='utf-8') as f:
                f.write(content)
            print(f"  ‚úÖ {changes_made} changements sauvegard√©s dans {file_path}")
        except Exception as e:
            print(f"  ‚ùå Erreur lors de la sauvegarde: {e}")
    else:
        print(f"  ‚ÑπÔ∏è  Aucun changement n√©cessaire dans {file_path}")
    
    return changes_made

def main():
    """
    Fonction principale pour traduire tous les fichiers species_info.
    """
    script_dir = os.path.dirname(os.path.abspath(__file__))
    species_info_dir = os.path.join(script_dir, "src", "data", "pokemon", "species_info")
    
    if not os.path.exists(species_info_dir):
        print(f"‚ùå R√©pertoire non trouv√©: {species_info_dir}")
        return 1
    
    total_changes = 0
    files_processed = 0
    
    # Traiter tous les fichiers *families.h
    for filename in os.listdir(species_info_dir):
        if filename.endswith("families.h"):
            file_path = os.path.join(species_info_dir, filename)
            changes = translate_pokemon_names_in_file(file_path)
            total_changes += changes
            files_processed += 1
    
    print(f"\nüìä R√©sum√©:")
    print(f"   Fichiers trait√©s: {files_processed}")
    print(f"   Traductions effectu√©es: {total_changes}")
    print(f"   ‚úÖ Traduction termin√©e!")
    
    return 0

